import math
#Penjumlahan
def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print("Hasilnya adalah ", hasil)
#Pengurangan
def kurang(bil1, bil2):
    hasil = bil1 - bil2
    print("Hasilnya adalah ", hasil)
#Perkalian
def kali(bil1, bil2):
    hasil = bil1 * bil2
    print("Hasilnya adalah ", hasil)
#Pembagian
def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print("Hasilnya adalah ", hasil)
#Pangkat
def pangkat(bil1, bil2):
    hasil = math.pow(bil1, bil2)
    print("Hasilnya adalah ", hasil)
#Akar
def akar(bil1):
    hasil = math.sqrt(bil1)
    print("Hasilnya adalah ", hasil)
#Logaritma
def log(bil1):
    hasil = math.log(bil1)
    print("Hasilnya adalah ", hasil)
#Sinus
def sin(bil1):
    hasil = math.sin(bil1)
    print("Hasilnya adalah ", hasil)
#Cosinus
def cos(bil1):
    hasil = math.cos(bil1)
    print("Hasilnya adalah ", hasil)
#Tangen
def tan(bil1):
    hasil = math.tan(bil1)
    print("Hasilnya adalah ", hasil)
